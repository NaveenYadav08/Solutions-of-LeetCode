vector<int> findSpiral(Node *root)
{
    //Your code here
    vector<int> ans;
    vector<int> temp;
    if(root==NULL) return ans;
    queue<Node*> q;
    q.push(root);
    q.push(NULL);
    int RtoL=true;
    while(!q.empty())
    {
        Node* P=q.front();
        q.pop();
        if(P!=NULL)
        {
            temp.push_back(P->data);
            if(P->left)
            q.push(P->left);
            if(P->right)
            q.push(P->right);
        }
        else 
        {   
            if(RtoL)
            {
                for(int i=temp.size()-1;i>=0;i--)
                {
                    ans.push_back(temp[i]);
                    
                }
                
            }
            else
            {
                for(auto x:temp)
                {
                    ans.push_back(x);
                    
                }
            }
            
            RtoL=!RtoL;
            temp.clear();
            //ğŸ”¥ğŸ”¥ğŸ”¥ğŸ”¥ğŸ”¥ğŸ”¥ğŸ”¥ğŸ”¥ğŸ”¥ğŸ”¥ğŸ”¥ğŸ”¥ğŸ”¥ğŸ”¥ğŸ”¥ğŸ”¥ğŸ”¥ğŸ”¥ğŸ”¥ğŸ”¥ğŸ”¥ğŸ”¥//
            if(!q.empty())
            q.push(NULL);
            
        }

    }
    return ans;
}
